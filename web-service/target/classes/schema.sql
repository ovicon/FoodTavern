DROP TABLE IF EXISTS FRUITS;
DROP TABLE IF EXISTS CHEESES;
DROP TABLE IF EXISTS SWEETS;

CREATE TABLE FRUITS(
	ID BIGINT AUTO_INCREMENT,
	NAME varchar(255)
);

CREATE TABLE CHEESES(
	ID BIGINT AUTO_INCREMENT,
	NAME varchar(255)
);

CREATE TABLE SWEETS(
	ID BIGINT AUTO_INCREMENT,
	NAME varchar(255)
);